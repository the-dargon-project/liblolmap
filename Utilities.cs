namespace Dargon.League.Maps {
   public class Color4 {
      public Color4(float r, float g, float b, float a) {
         this.r = r;
         this.g = g;
         this.b = b;
         this.a = a;
      }

      public float r;
      public float g;
      public float b;
      public float a;
   }

   public class Float2 {
      public Float2(float x, float y) {
         this.x = x;
         this.y = y;
      }

      public float x;
      public float y;
   }

   public class Float3 {
      public Float3(float x, float y, float z) {
         this.x = x;
         this.y = y;
         this.z = z;
      }

      public float x;
      public float y;
      public float z;
   }

   public class Float4 {
      public Float4(float x, float y, float z, float w) {
         this.x = x;
         this.y = y;
         this.z = z;
         this.w = w;
      }

      public float x;
      public float y;
      public float z;
      public float w;
   }
}
